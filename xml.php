function ifSet($arr, $default = '') {
  return (isset($arr[0]) && !is_array($arr[0])) ? $arr[0] : $default;
}

function getSubtree($arr, $name) {
  foreach ($arr as $a => $b) {
    if (is_array($b)) {
      foreach ($b as $c => $d) {
        if ($c == $name) {
          return $d;
        }
      }
    }
  }
  return array();
}


function array2xml($mixed, $domElement=null, $DOMDocument=null) {
    if (is_null($DOMDocument)) {
        $DOMDocument = new DOMDocument();
        $DOMDocument->formatOutput = true;
        array2xml($mixed, $DOMDocument, $DOMDocument);
        return $DOMDocument->saveXML($DOMDocument->documentElement);
    } else {
        if (is_array($mixed)) {
            foreach ($mixed as $index => $mixedElement) {
                if (is_int($index)) {
                      array2xml($mixedElement, $domElement, $DOMDocument);
                } else {
                    if ($index[0] == "@") {
                      $index = substr($index, 1);
                      $node = $domElement->setAttribute($index, $mixedElement);

                    } else {
                      $element = $DOMDocument->createElement($index);
                      $domElement->appendChild($element);
                      $node = $element;
                      array2xml($mixedElement, $node, $DOMDocument);
                    }
                }
            }
        } else {
            $domElement->appendChild($DOMDocument->createTextNode($mixed));
        }
    }
}

function xmlObj2array($obj) {
  $result = array();

  $classname = get_class($obj);
  switch($classname) {

    case "DOMDocument":
    case "DOMElement":
      foreach ($obj->childNodes as $node) {
        if ($node->nodeName == "#text") {
          if (trim($node->nodeValue) !== "") {
            array_push($result, trim($node->nodeValue));
          }
        } else {
          array_push($result, array(trim($node->nodeName) => xmlObj2array($node)));
        }
      }
      if ($obj->hasAttributes()) {
        foreach ($obj->attributes as $attr) {
          array_push($result, array(trim('@'.$attr->nodeName) => trim($attr->nodeValue)));
        }
      }


    case "DOMText": 
    case "DOMComment": // skip
    break;
    default:
//      echo $classname. " "; // debug
    break;
  } 


  return $result;
}


function xml2array($data) {
  $Doc = new DOMDocument();
  $Doc->loadXML($data);
  return xmlObj2array($Doc);
}



